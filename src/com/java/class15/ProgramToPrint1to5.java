package com.java.class15;
public class ProgramToPrint1to5 {
    //Write a program to print numbers from 1-5;
    public static void main(String[] args) {
        int i=1;                             //initialization
        while (i<=5){                         // condition
            System.out.println(i);
            i++;                            //increment&decrement
        }
    }
}
