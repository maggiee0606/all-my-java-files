package com.java.class04;
//Program to check given number is odd or even

// 13 - odd, 12 - even
public class OddEven {
    public static void main(String[] args) {
        int num = 13;
        if (num % 2 == 0) {
            System.out.println("odd");
        } else {
            System.out.println("even");

        }
    }
}